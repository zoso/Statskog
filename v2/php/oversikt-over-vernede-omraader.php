                        <h2>Oversikt over vernede områder</h2>
                        <p>Statens eierskap er brukt som virkemiddel i etablering av nasjonale verneområder, som nasjonalparker og barskogvernområder. De første nasjonalparkene i Norge ble i hovedsak etablert på statens grunn, med unntak av Hardangervidda Nasjonalpark.</p>
                        <p>I 2003 inngikk Statskog en samarbeidsavtale med Direktoratet for naturforvaltning (DN) om vern av skog på Statskogs eiendommer. Formålet med samarbeidsavtalen har vært å gjennomføre tiltak og definere oppgaver i forhold til utvidet skogvern, samt sikre en rasjonell gjennomføring av verneprosesser der Statskog er involvert. Det er DN som er vernemyndigheten. Statskogs rolle i dette samarbeidet er å være grunneier, med visse forvaltningsmessige oppgaver i statsallmenningene i henhold til fjell- og allmenningslovene. Kartet viser all vernet grunn i Norge, på både Statskogs og andre grunneieres eiendommer.</p>
                        <p>Produktiv skog</p>
                        <p>Statskog forvalter en femtedel av fastlands-Norge. 17 prosent av denne grunnen består av skog, resten ligger over skoggrensen. Produktiv skog, det vil si områder med god nok skog til at det kan drives aktivt skogbruk, er det på 47 prosent av Statskogs skogarealer. Statskogs produktive skog utgjør seks prosent av den totale produktive skogen i Norge.</p> 
                        <p>Skogsbilveier</p>
                        <p>Statskog har ansvar knyttet til 1 475 skogbilveianlegg og 6 300 km skogsbilvei. Ofte er skogsbilveiene til nytte for langt flere enn skogeierne, da særlig til friluftsbruk eller ulike eksterne rettighetshavere. De aller fleste veiene er på foretakets egne eiendommer. På enkelte veier har Statskog ansvar alene, mens andre veier er fellesveier. Det er utfordrende å holde alle veiene ved like, særlig i de periodene det ikke tas ut skog. Derfor er det bomavgift på noen av veiene og noen veier er stengt for allmenn ferdsel på grunn av dårlig standard. For hver kubikkmeter tømmer Statskog avvirker, bruker vi 20-25 kroner til bygging, opprusting og vedlikehold av skogsveier</p>
                        <p>Solgte arealer i 2011 og 2012</p>
                        <p>For å betale ned lånet Statskog fikk fra staten ved kjøp av Orklas skogeiendommer og samtidig få en bedre arrondert, eller driftsmessig, eiendomsmasse, er Statskog i ferd med å selge ut til sammen om lag 500 000 daa bestående av spredte skogeiendommer Statskog eide før kjøpet av Orklas skogeiendommer. Kartet viser hvilke eiendommer som er solgt så langt. Salget avsluttes i 2017.</p>
                        <p>Energiprosjekter</p>
                        <p>Statskog har i samarbeid med andre berørte grunneiere og kraftaktører fått konsesjon på fire kraftverk, ett av dem vil komme i drift i 2013. Ved utgangen av 2012 var det sendt inn ytterligere 41 søknader om bygging av fornybar kraftproduksjon til Norges vassdrags- og energidirektorat (NVE). Kartet viser hvor disse til sammen 45 kraftverkene ligger/planlegges.</p>
                        <p>Arealinndeling storviltjakt</p>
                        <p>Statskog SF har jaktfelt i de fleste av landets fylker med elgjakt (unntatt Finnmark). Jaktfeltene ligger ved kysten, i skogen og opp mot høgfjellet og snaufjellet. Jegere kan velge mellom jakt nær bilvei eller med flere timers gange inn i jaktfeltet. I dette brede spekteret av tilbud, kan mange jaktlag finne sitt drømmeterreng. Mange jaktfelt er delt i perioder, både for å gi flere jegere/jaktlag tilbud, og for å sikre at stort nok antall dyr blir felt. Årlig kvote som forvaltes av Statskog er i underkant av 1 500 elg og i underkant av 200 hjort. Det gid også tilbud om villreinjakt og rådyrjakt på Statskogs grunn. Tildeling av tilbud skjer etter søknad og trekning. 
                        </p>
