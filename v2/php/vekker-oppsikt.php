                    <h2>Statskog vekker oppmerksomhet</h2>
                    <p>Statskogs har lenge arbeidet med å finne løsninger som operasjonaliserer strategien. Nå vekker dette arbeidet ekstern oppsikt. Det har blant annet gitt seg utslag i at Statskog ved Ronald Gåsvær nylig ble utfordret til å være med på Beslutningsstøttedagen (fast møteplass der viksomhetsstyring står i fokus). 
-   I Statskog har vi jobbet med løsninger for å gjøre strategien styrbar ganske lenge nå. Vi ønsker system for å kunne følge med og måle at strategien blir fulgt. Rett og slett en slags støttesystemer for å nå målene våre, sier økonomisjef Ronald Gåsvær.
Dette er det ikke mange som er gode på. Det var gjennom samarbeid med Agresso at invitasjonen til å presentere arbeidet på Beslutningsstøttedagen kom. Agresso synes måten Statskog har jobbet med måling av strategioppnåelse på var så interessant at de utfordret Statskog til å være med. 
-   De ga uttrykk for at de er særlig imponert over måten vi har jobbet forut for utvikling av målesystemer. Mange går rett på verktøyet uten å reflektere over hva en vil og hvorfor, og da oppnår en ikke like mye, sier Gåsvær. Og legger til at bedrifter som ønsker å gjøre dette må gjøre mye av jobben selv. En vil ikke lykkes om alt settes bort til eksterne.
På Beslutningsstøttedagen var 150 store og mellomstore norske bedrifter representert. Gåsvær og Statskog er i etterkant blitt kontaktet av flere som ønsker å høre mer fordi de skal i gang med samme prosess.
-   Det vi har gjort og budskapet vårt vakte oppsikt. Vi fikk mye ros for å ha tenkt og agert riktig, sier Gåsvær.
Hva er det så Statskog har gjort på dette området?
Ifølge Gåsvær begynte arbeidet så snart den nye strategien var på plass. Det startet med et ønske om å se på hvordan Statskog kunne lage verktøy som kan hjelpe oss i å se om vi beveger oss i den retningen strategien sier vi skal. 
-   Vi tok først en intern prosess på å finne måleindikatorer som kunne si noe om måloppnåelse, forteller Gåsvær.
Det var Gåsvær som hadde ideen til hvordan arbeidet skulle legges opp. Han har hele tiden jobbet tett sammen med finansdirektør og administrerende direktør. Og i et tett samarbeid med forretningsområdene og regionene kom måleindikatorene på plass. 
-   Det har vært helt avgjørende med bred involvering – og med forankring i toppledelsen, sier Gåsvær.
Så snart indikatorene var på plass, så de som var engasjert i prosessen at Statskog hadde behov for et verktøy for å kunne gjennomføre målingene best mulig.
-   Vi vurderte et par løsninger, og endte opp med å velge Agresso. Siden vi hadde en avtale med dem fra før, fikk vi en klar fordel i at det ble enklere å integrere. Det er for øvrig helt avgjørende at systemet vi velger har funksjonalitet nok til at målingene fungerer, understreker Gåsvær.
Implementeringen av denne virksomhetsstyringen går over to faser. Først kom økonomirapporteringen på plass (Exie). Deretter begynte jobben med å utarbeide en samling av konkrete indikatorer i en måltavle som vil bli realisert i løsningen. 
-   Vi kaller det virksomhetsstyring fordi dette er mye mer enn økonomistyring. Alt som er viktig for å nå målene kommer inn, sier Gåsvær. 
Han trekker fram tre eksempler på ikke-økonomiske måleindikatorer: Kvalitetsavvik i skog, realisering av eiendomsutviklingsareal i regionene og framdriftsmåling innenfor realisering av energiprosjekt. Det handler om mer enn fokus på økonomi. Derfor er også en av suksessfaktorene for å lykkes i dette arbeidet at prosjektlederen er med i alle ledd, også det som ikke angår økonomi.
-   Vedkommende må skjønne helheten og tenkinga bak målene som er satt, slik at det nettopp blir virksomhetsstyring og ikke «bare» økonomistyring, sier Gåsvær.
Han forteller at måltavla de har kommet fram til er en første versjon. Det er krevende å finne gode indikatorer, så de vil måtte justeres underveis. Dette vil være en kontinuerlig prosess uten fasitsvar. 
</p>
